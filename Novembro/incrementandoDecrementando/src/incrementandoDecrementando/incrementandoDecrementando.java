package incrementandoDecrementando;

public class incrementandoDecrementando {

	public static void main(String[] args) {
		//incrementando
		System.out.println("Incrementando");
		for (int vezes = 1; vezes <= 10; vezes++) {
				System.out.println(vezes + " ");

	}	System.out.println();
		//decrementando
		System.out.println("Decrementando");	
		for (int vezes = 10; vezes >= 1; vezes--) {
			System.out.println(vezes + " ");

}
}
}
