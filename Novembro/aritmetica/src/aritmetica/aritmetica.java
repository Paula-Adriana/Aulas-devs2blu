package aritmetica;

public class aritmetica {

	public static void main(String[] args) {
		int numero1 = 10, numero2 = 20, resultado = 0;
		
		resultado = numero1 + numero2;
		System.out.println(resultado);
		
		resultado = numero1 - numero2;
		System.out.println(resultado);
		
		numero2 = 2;		
		resultado = numero1 * numero2;
		System.out.println(resultado);

		resultado = numero1 / numero2;
		System.out.println(resultado);

	}

}